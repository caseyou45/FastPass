package business;

/**
 *
 * @author CWilson
 */
public class Airport {

    private int airportId;
    private String airportName;
    private String airportIataCode;
    private String airportCode;
    private double airportLat;
    private double airportLng;
    private String airportCountryCode;
    private String airportCity;
    private String airportState;

    public int getAirportId() {
        return airportId;
    }

    public void setAirportId(int airportId) {
        this.airportId = airportId;
    }

    public String getAirportName() {
        return airportName;
    }

    public void setAirportName(String airportName) {
        this.airportName = airportName;
    }

    public String getAirportIataCode() {
        return airportIataCode;
    }

    public void setAirportIataCode(String airportIataCode) {
        this.airportIataCode = airportIataCode;
    }

    public String getAirportCode() {
        return airportCode;
    }

    public void setAirportCode(String airportCode) {
        this.airportCode = airportCode;
    }

    public double getAirportLat() {
        return airportLat;
    }

    public void setAirportLat(double airportLat) {
        this.airportLat = airportLat;
    }

    public double getAirportLng() {
        return airportLng;
    }

    public void setAirportLng(double airportLng) {
        this.airportLng = airportLng;
    }

    public String getAirportCountryCode() {
        return airportCountryCode;
    }

    public void setAirportCountryCode(String airportCountryCode) {
        this.airportCountryCode = airportCountryCode;
    }

    public String getAirportCity() {
        return airportCity;
    }

    public void setAirportCity(String airportCity) {
        this.airportCity = airportCity;
    }

    public String getAirportState() {
        return airportState;
    }

    public void setAirportState(String airportState) {
        this.airportState = airportState;
    }

}
